using System;

// variable declaration and initialization (recommended in one line)
string firstName = "Bob";

// retrieve the value from the variable
Console.WriteLine(firstName);

// reassign the variable
firstName = "Liem";
Console.WriteLine(firstName);
firstName = "Isabella";
Console.WriteLine(firstName);
firstName = "Yasmin";
Console.WriteLine(firstName);

// implicitly typed local variable
