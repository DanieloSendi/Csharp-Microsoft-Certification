using System;

// character
Console.WriteLine('b');

// string
Console.WriteLine("Hello World!");

// integer
Console.WriteLine(123);

// float with iteral suffix F or f
Console.WriteLine(0.25F);

// double decimal number is entered without a literal suffix
Console.WriteLine(2.625);

// decimal with m or M literal suffix.
Console.WriteLine(12.39816m);

// boolean
Console.WriteLine(true);
Console.WriteLine(false);